#include "3-calc.h"
/**
 * op_add - a function that add two intergers.
 * @a: an integer.
 * @b: another integer.
 * Return: returns the sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a function that substract two integers.
 * @a: an integer.
 * @b: another integer.
 * Return: the difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a function that multiply two integers.
 * @a: an integer.
 * @b: another integer.
 * Return: the product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a function that divide two integers.
 * @a: an integer.
 * @b: another integer.
 * Return: the result of the division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a function that returns the remainder
 * of the division of two integers.
 * @a: an integer.
 * @b: another integer.
 * Return: the remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
